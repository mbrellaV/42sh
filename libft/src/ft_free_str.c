#include "libft.h"

int		ft_free_str(char **arr)
{
	int	i;

	if (!arr)
		return (1);
	i = -1;
	while (arr[++i])
	{
		ft_strdel(&arr[i]);
	}
	free(arr);
	arr = NULL;
	return (1);
}
